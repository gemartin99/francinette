#include <stdio.h>

void	ft_swap(int *a, int *b);

int	main(void)
{
	int	a;
	int	b;

	a = 13;
	b = 23;
	ft_swap(&a, &b);
	printf("%d %d\n", a, b);
	a = -23;
	b = 45;
	ft_swap(&a, &b);
	printf("%d %d\n", a, b);
}
